package com.tcs.bits.dynamicautomation.to;

public class Output {

	private String totalTestCases;
	private String passCount;
	private String failCount;
	private String logsPath;
	private String screenshotsPath;
	public String getTotalTestCases() {
		return totalTestCases;
	}
	public void setTotalTestCases(String totalTestCases) {
		this.totalTestCases = totalTestCases;
	}
	public String getPassCount() {
		return passCount;
	}
	public void setPassCount(String passCount) {
		this.passCount = passCount;
	}
	public String getFailCount() {
		return failCount;
	}
	public void setFailCount(String failCount) {
		this.failCount = failCount;
	}
	public String getLogsPath() {
		return logsPath;
	}
	public void setLogsPath(String logsPath) {
		this.logsPath = logsPath;
	}
	public String getScreenshotsPath() {
		return screenshotsPath;
	}
	public void setScreenshotsPath(String screenshotsPath) {
		this.screenshotsPath = screenshotsPath;
	}
	
	
	
	
}
